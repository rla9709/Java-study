package assignment;

public class OperationEx {

	public static void main(String[] args) {
		int age = 27;
		System.out.println(age);
		//대입 연산자
		
		int num = 10;
		int num2 = -num;
		System.out.println(num2);
		
		
		int num3 = 10;
		System.out.println(++num3);
		
		int num4 = 10;
		System.out.println(num4++);
		System.out.println(num4);

	}

}
